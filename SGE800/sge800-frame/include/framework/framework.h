/******************************************************************************
*	函数:	framework_init
*	功能:	平台框架初始化（时间管理、消息调度、数据访问；设备部分的初始化在业务模块调用）
*	参数:
*	返回:	0				-	成功
 			-ERR_SYS		-	系统错误
 			-ERR_BUSY		- 	模块已初始化
			-ERR_NODEV		-	无此设备
*	说明:	此函数由main.c调用。
 ******************************************************************************/
int framework_init (void);

/******************************************************************************
*	函数:	framework_start
*	功能:	平台框架启动（建立各业务模块对应的线程）
*	参数:
*	返回:	0				-	成功
 			-ERR_SYS		-	系统错误
*	说明:	此函数由main.c调用。
 ******************************************************************************/
int framework_start (void);

/******************************************************************************
*	函数:	module_register
*	功能:	业务模块注册（为了获得业务模块请求的线程信息）
*	参数:	obj				-	业务模块对象指针
*	返回:	0				-	成功
 			-ERR_INVAL		-	参数错误
*	说明:
 ******************************************************************************/
int module_register (struct BASE *obj);


